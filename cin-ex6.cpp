#include <iostream>

int main() {
    int y = 10;  // Define an integer variable
    std::cout << "Memory address of y: " << &y << std::endl;

    // Define a pointer and assign it the address of y
    int *x = &y;

    // Print the value of y and the value stored at pointer x before modifying y
    std::cout << "Value of y: " << y << std::endl;
    std::cout << "Value pointed to by x (before changing y): " << *x << std::endl;

    // Modify y
    y = 20;

    // Print the value of y and the value stored at pointer x after modifying y
    std::cout << "Value of y after modification: " << y << std::endl;
    std::cout << "Value pointed to by x (after changing y): " << *x << std::endl;

    return 0;
}
